package com.conducthq.auspost.model.data;

public class Feedback {

    int rating;
    String comments;

    public Feedback() {
    }

    public int getRating() {
        return rating;
    }

    public void setRating(int rating) {
        this.rating = rating;
    }

    public String getComments() {
        return comments;
    }

    public void setComments(String comments) {
        this.comments = comments;
    }
}
